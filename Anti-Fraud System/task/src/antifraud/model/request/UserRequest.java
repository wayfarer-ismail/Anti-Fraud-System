package antifraud.model.request;

public class UserRequest {
    private String name;

    private String username;

    private String password;

    public UserRequest() {
        // Default constructor
    }

    // getters and setters

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }
}
